#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0' && *s != (char)c)
		s++;
	if ((*s == '\0' && (char)c == '\0') || (*s != '\0'))
		return ((char *)s);
	else
		return (NULL);
}
